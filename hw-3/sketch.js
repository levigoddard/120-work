// Declare the setup function
function setup() {

    // create a canvas 800px wide and 800px high
    createCanvas( 800, 800 );

    // color the background 'grey'
    background( 'grey' );
}

// Declare the draw function
function draw() {

    // tell p5 to print the text string "Hello World!"
    // This is going to be positioned at ( x: 60, y: 60 ).
    text( "Hello World!", 60, 60 );
}
